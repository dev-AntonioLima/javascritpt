// Como adicionar elementos

const numeros = [1,2,3];

// propriedade que insere valores no início de um array.
numeros.unshift('inicio');
console.log(numeros);

// propriedade que insere valores no meio de um array.
numeros.splice(1,0,'meio');
console.log(numeros);

// propriedade que insere valores no final de um array.
numeros.push('final');
console.log(numeros);





